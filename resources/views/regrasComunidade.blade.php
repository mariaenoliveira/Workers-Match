        <h1>Regras da Comunidade</h1>

        
        Todos os membros devem se tratar com respeito e agir de maneira profissional em todas as interações. Comportamentos inadequados, 
        ofensivos, discriminatórios ou desrespeitosos não serão tolerados.
        


        <h3>Informações Verídicas</h3>

        
        Os membros devem fornecer informações precisas e verdadeiras em seus perfis e currículos. Qualquer informação 
        falsa ou enganosa pode resultar na remoção do usuário da plataforma.


        <h3>Confidencialidade e Privacidade</h3>

        A confidencialidade e a privacidade dos usuários devem ser respeitadas em todos os momentos. As informações pessoais dos membros não 
        devem ser compartilhadas sem o consentimento explícito dos mesmos.
        


        <h3>Equalidade de Oportunidades</h3>

        Todos os membros devem ter acesso igualitário às oportunidades de emprego, independentemente de sua origem, raça, gênero, orientação sexual, 
        religião, idade ou qualquer outra característica protegida por lei.
        
        <hr class="espaco">

        <h1>Comunicação Respeitosa</h1>


        As interações entre os membros devem ser conduzidas de forma respeitosa e profissional. Linguagem inadequada, agressiva ou ofensiva não será tolerada.

            
        <h3>Feedback Construtivo</h3>

        Os membros são incentivados a fornecer feedback construtivo uns aos outros, mas sempre de forma respeitosa e objetiva. 
        Críticas pessoais ou ataques não são permitidos.

            
        <h3>Transparência</h3>
            
        A empresa deve fornecer informações claras e transparentes sobre seus serviços, políticas e termos de uso. 
        Qualquer alteração nessas políticas deve ser comunicada claramente aos membros.
            

        <h3>Proteção contra Fraudes e Golpes</h3>

        <p>
            A empresa deve implementar medidas para proteger os membros contra fraudes, golpes e atividades maliciosas. Qualquer suspeita de atividade fraudulenta deve ser relatada imediatamente.
        </p>


        <h3>Suporte ao Cliente</h3>  
        
        <p>A empresa deve oferecer suporte ao cliente eficiente e responsivo para ajudar os membros com dúvidas, problemas ou preocupações relacionadas à plataforma.</p>


        <h3>Alterações nestas regras da Comunidade</h3>

        <p>
            Essas regras podem ser atualizada periodicamente. Recomendamos que você reveja esta página regularmente para se manter informado sobre quaisquer mudanças.
            Ao utilizar nossos serviços, você concorda com a coleta, uso e compartilhamento de suas informações pessoais de acordo com as regras da comunidade.
            Se tiver alguma dúvida ou preocupação sobre as regras da comunidade ou sobre o uso de suas informações pessoais, entre em contato conosco.
        </p>

        <h1>Responsabilidade Social e Ética</h1>

        <h4>A empresa deve agir de maneira socialmente responsável e ética em todas as suas operações, promovendo o bem-estar dos membros e contribuindo positivamente para a sociedade.
            <br><br><br>
            Essas regras da comunidade visam garantir um ambiente seguro, justo e profissional para todos os membros da plataforma que estão na busca de um emprego.
        </h4>
        
